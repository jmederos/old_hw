#import "ViewController.h"

#define BUFFER_OFFSET(i) ((char *)NULL + (i))



// Uniform index.
enum
{
    UNIFORM_MODELVIEWPROJECTION_MATRIX,
    UNIFORM_NORMAL_MATRIX,
    NUM_UNIFORMS
};
GLint uniforms[NUM_UNIFORMS];

// Attribute index.
enum
{
    ATTRIB_VERTEX,
    ATTRIB_NORMAL,
    ATTRIB_COLOR,
    NUM_ATTRIBUTES
};


GLfloat leaf_vertices[] = {
    0.0,0.0,0.0,   0.5,0.9,0.3,
    0.25*.5,0.19*.5,0.0, 0.1 ,0.5,0.1,
    0.30*.5,0.48*.5,0.0, 0.4,0.8,0.3,
    0.27*.5,0.62*.5,0.0, 0.1,0.5,0.1,
    0.0*.5,1.0*.5,0.0,   0.5,0.9,0.3,
    -0.27*.5,0.62*.5,0.0,  0.1,0.5,0.3,
    -0.30*.5,0.48*.5,0.0,  0.4,0.8,0.3,
    -0.25*.5,0.19*.5,0.0,  0.1,0.5,0.1
};

GLubyte leaf_indicies[] = {
    0, 1, 2, 
    0, 2, 3,
    0, 3, 4, 
    0, 4, 5,
    0, 5, 6,
    0, 6, 7
};

@interface ViewController () {
    GLuint _program;
    
    GLKMatrix4 _modelViewProjectionMatrix;
    GLKMatrix3 _normalMatrix;
    float _rotation;
    
    GLuint _vertexArray;
    GLuint _vertexBuffer;
}
@property (strong, nonatomic) EAGLContext *context;
@property (strong, nonatomic) GLKBaseEffect *effect;
@property GLKMatrix4 modelViewMatrix;
@property GLKMatrix4 baseModelViewMatrix;
@property GLKMatrix4 zoomMatrix;

- (void)setupGL;
- (void)tearDownGL;

- (BOOL)loadShaders;
